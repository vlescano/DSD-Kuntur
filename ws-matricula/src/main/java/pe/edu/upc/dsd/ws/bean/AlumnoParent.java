package pe.edu.upc.dsd.ws.bean;

public class AlumnoParent {

	private String codigoAlumno;
	private String codigoParent;
	private String codigoFamilia;
	public String getCodigoAlumno() {
		return codigoAlumno;
	}
	public void setCodigoAlumno(String codigoAlumno) {
		this.codigoAlumno = codigoAlumno;
	}
	public String getCodigoParent() {
		return codigoParent;
	}
	public void setCodigoParent(String codigoParent) {
		this.codigoParent = codigoParent;
	}
	public String getCodigoFamilia() {
		return codigoFamilia;
	}
	public void setCodigoFamilia(String codigoFamilia) {
		this.codigoFamilia = codigoFamilia;
	}
	

}